const reverseAndSort = (arr) => {
    let reverseArr = arr.reverse();
    console.log(`Reversed Array`);
    console.log(reverseArr);

    let reverseSort = reverseArr.sort();
    console.log(`Reversed Sort Array`);
    console.log(reverseSort);
}

reverseAndSort(['b', 'a', 'c', 'v', 'd', 'j', 'o', 'e', 'w', 'g', 'r']);



